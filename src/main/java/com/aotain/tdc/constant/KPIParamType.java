package com.aotain.tdc.constant;

/**
 * 质量指标类型
 * @author 凯哥
 *
 */
public enum KPIParamType {
	SUCCESS_PERCENT("连接成功率"),
	RESP_DELAY("应答时延"),
	SERVER_DELAY("服务端时延"),
	CLIENT_DELAY("客户端时延"),
	TIMEOUT_PERCENT("连接超时率"),
	UP_RETRANS_PERCENT("上行重传率"),
	DOWN_RETRANS_PERCNET("下行重传率");
	private String type;
	private KPIParamType(String type){
		this.setType(type);
	}
	public String getType() {
		return type;
	}
	public void setType(String type) {
		this.type = type;
	}
}
