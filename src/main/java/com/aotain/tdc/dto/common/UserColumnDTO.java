package com.aotain.tdc.dto.common;

public class UserColumnDTO {
	private Integer operID;
	private Integer dictId;
	//1表示显示，2表示隐藏
	private Integer tag;
	
	public UserColumnDTO() {
	}
	
	public UserColumnDTO(Integer userId, Integer dictId, Integer tag) {
		super();
		this.operID = userId;
		this.dictId = dictId;
		this.tag = tag;
	}
	public Integer getUserId() {
		return operID;
	}
	public void setUserId(Integer userId) {
		this.operID = userId;
	}
	public Integer getDictId() {
		return dictId;
	}
	public void setDictId(Integer dictId) {
		this.dictId = dictId;
	}
	public Integer getTag() {
		return tag;
	}
	public void setTag(Integer tag) {
		this.tag = tag;
	}
	
}
