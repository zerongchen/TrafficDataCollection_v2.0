package com.aotain.tdc.dto.common;

import java.io.Serializable;
/**
 * 6个质量指标实体类
 * @author 凯哥
 * 
 */

public class KPIBean implements Serializable {
	private static final long serialVersionUID = -7854430930914397178L;
	//连接成功率
	private float successPercent;
	//应答时延
	private float respDelay;
	//服务端时延
	private float serverDelay;
	//客户端时延
	private float clientDelay;
	//连接超时率
	private float timeoutPercent;
	//上行重传率
	private float upRetransPercent;
	//下行重传率
	private float downRetransPercent;
	public float getSuccessPercent() {
		return successPercent;
	}
	public float getRespDelay() {
		return respDelay;
	}
	public float getServerDelay() {
		return serverDelay;
	}
	public float getClientDelay() {
		return clientDelay;
	}
	public float getTimeoutPercent() {
		return timeoutPercent;
	}
	public float getDownRetransPercent() {
		return downRetransPercent;
	}
	public void setSuccessPercent(float successPercent) {
		this.successPercent = successPercent;
	}
	public void setRespDelay(float respDelay) {
		this.respDelay = respDelay;
	}
	public void setServerDelay(float serverDelay) {
		this.serverDelay = serverDelay;
	}
	public void setClientDelay(float clientDelay) {
		this.clientDelay = clientDelay;
	}
	public void setTimeoutPercent(float timeoutPercent) {
		this.timeoutPercent = timeoutPercent;
	}
	public void setDownRetransPercent(float downRetransPercent) {
		this.downRetransPercent = downRetransPercent;
	}
	public float getUpRetransPercent() {
		return upRetransPercent;
	}
	public void setUpRetransPercent(float upRetransPercent) {
		this.upRetransPercent = upRetransPercent;
	}
}
